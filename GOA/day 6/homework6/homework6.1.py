#შექმენით თქვენი daily routine-ისგან ციკლი
day = 1
while day <= 31:
    print("day ", day)
    day = day + 1
    print("wake up")
    print("go to school")
    print("work out")
    print("study")
    print("sleep")
    if day <= 31:
        print("day ", day)
        print("wake up")
        print("go to school")
        print("rest")
        print("study")
        print("sleep")


